{-# LANGUAGE OverloadedStrings #-}  -- Расширение для удобной работы со строками (необязательно)

-- | Модуль Main — точка входа в программу.
module Main where

-- Импорт стандартной библиотеки (Prelude) происходит автоматически.
-- Здесь мы ничего дополнительного не импортируем, всё необходимое уже есть.

--------------------------------------------------------------------------------
-- 1. Обычные функции с явно заданными типами
--------------------------------------------------------------------------------

-- | Функция сложения двух целых чисел.
-- Тип: Int -> Int -> Int (принимает два Int, возвращает Int).
add :: Int -> Int -> Int
add x y = x + y   -- Тело функции: складываем x и y.

-- | Функция вычитания (демонстрация).
sub :: Int -> Int -> Int
sub x y = x - y

--------------------------------------------------------------------------------
-- 2. Полиморфные функции (переменная типа 'a')
--------------------------------------------------------------------------------

-- | Функция тождества: возвращает свой аргумент без изменений.
-- Тип: a -> a, где a — любой тип.
identity :: a -> a
identity x = x

-- | Функция, возвращающая длину списка (стандартная length уже есть,
-- но мы покажем её полиморфизм явно).
-- Тип: [a] -> Int — работает со списком элементов любого типа.
myLength :: [a] -> Int
myLength []     = 0       -- Базовый случай: пустой список
myLength (_:xs) = 1 + myLength xs  -- Рекурсивный подсчёт

--------------------------------------------------------------------------------
-- 3. Операторы: инфиксная и префиксная нотации
--------------------------------------------------------------------------------

-- | Функция, использующая оператор '+' в инфиксной форме.
infixSum :: Int -> Int -> Int
infixSum x y = x + y   -- '+' стоит между аргументами.

-- | Та же операция, но оператор '+' применён в префиксной форме
-- (как обычная функция).
prefixSum :: Int -> Int -> Int
prefixSum x y = (+) x y   -- Оператор заключён в скобки и стоит перед аргументами.

-- | Демонстрация пользовательского оператора (создадим свой бинарный оператор).
-- Оператор '|+|' будет складывать два числа.
(|+|) :: Int -> Int -> Int
x |+| y = x + y

--------------------------------------------------------------------------------
-- 4. Классы типов: Eq, Ord, Num
--------------------------------------------------------------------------------

-- | Функция сравнивает два значения и возвращает строку с результатом.
-- Ограничения: типы должны принадлежать классам Ord (сравнение) и Show (преобразование в строку).
compareValues :: (Ord a, Show a) => a -> a -> String
compareValues x y =
    if x < y then show x ++ " < " ++ show y
    else if x == y then show x ++ " == " ++ show y
    else show x ++ " > " ++ show y

-- | Функция, демонстрирующая работу с числами (класс Num).
-- Просто умножаем два числа (тип может быть любым числовым).
multiply :: Num a => a -> a -> a
multiply x y = x * y

--------------------------------------------------------------------------------
-- 5. Функции высшего порядка (принимают функции как аргументы)
--------------------------------------------------------------------------------

-- | Применяет переданную функцию f ко всем элементам списка.
-- Тип: (a -> b) -> [a] -> [b] — функция и список.
applyMap :: (a -> b) -> [a] -> [b]
applyMap f xs = map f xs   -- стандартный map

-- | Фильтр, оставляющий только элементы, удовлетворяющие предикату.
-- Демонстрация передачи предиката.
keepEven :: [Int] -> [Int]
keepEven xs = filter even xs   -- even — встроенная функция проверки чётности.

--------------------------------------------------------------------------------
-- 6. Точка входа в программу: main
--------------------------------------------------------------------------------

main :: IO ()   -- main имеет тип IO (), т.е. выполняет действия ввода-вывода.
main = do
    -- Вывод заголовка
    putStrLn "=============================================="
    putStrLn "   Демонстрация функций и операторов в Haskell"
    putStrLn "=============================================="

    -- ---- 1. Обычные функции ----
    putStrLn "\n--- 1. Обычные функции ---"
    putStrLn $ "add 3 5 = " ++ show (add 3 5)          -- Вызов add
    putStrLn $ "sub 10 4 = " ++ show (sub 10 4)        -- Вызов sub

    -- ---- 2. Полиморфные функции ----
    putStrLn "\n--- 2. Полиморфные функции ---"
    putStrLn $ "identity 42 = " ++ show (identity 42)   -- с целым числом
    putStrLn $ "identity \"Hello\" = " ++ identity "Hello"  -- со строкой (без show, т.к. строка уже)
    putStrLn $ "myLength [1,2,3] = " ++ show (myLength [1,2,3])   -- список Int
    putStrLn $ "myLength [\"a\",\"b\",\"c\",\"d\"] = " ++ show (myLength ["a","b","c","d"]) -- список String

    -- ---- 3. Операторы ----
    putStrLn "\n--- 3. Операторы ---"
    putStrLn $ "infixSum 10 20 = " ++ show (infixSum 10 20)
    putStrLn $ "prefixSum 10 20 = " ++ show (prefixSum 10 20)
    putStrLn $ "5 |+| 7 = " ++ show (5 |+| 7)   -- пользовательский оператор в инфиксной форме
    putStrLn $ "(|+|) 5 7 = " ++ show ((|+|) 5 7) -- тот же оператор в префиксной форме

    -- ---- 4. Классы типов (Eq, Ord, Num) ----
    putStrLn "\n--- 4. Классы типов ---"
    putStrLn $ "compareValues 5 10 => " ++ compareValues 5 10
    putStrLn $ "compareValues 10 10 => " ++ compareValues 10 10
    putStrLn $ "compareValues 15 7 => " ++ compareValues 15 7
    putStrLn $ "multiply 6 7 = " ++ show (multiply 6 7)
    putStrLn $ "multiply 2.5 3.0 = " ++ show (multiply 2.5 3.0)   -- работает и для Float/Double

    -- ---- 5. Функции высшего порядка ----
    putStrLn "\n--- 5. Функции высшего порядка ---"
    let square x = x * x   -- локальное определение функции возведения в квадрат
    let numbers = [1,2,3,4,5]
    putStrLn $ "applyMap square [1,2,3,4,5] = " ++ show (applyMap square numbers)
    -- Использование анонимной функции (лямбда)
    putStrLn $ "map (\\x -> x * 2) [1,2,3,4,5] = " ++ show (map (*2) numbers)
    putStrLn $ "keepEven [1..10] = " ++ show (keepEven [1..10])

    -- ---- 6. Дополнительно: использование оператора ++ для конкатенации строк ----
    putStrLn "\n--- 6. Прочие примеры ---"
    let greeting = "Hello" ++ " " ++ "World!"
    putStrLn $ "Конкатенация: " ++ greeting

    -- Завершение программы
    putStrLn "\nПрограмма успешно завершена."