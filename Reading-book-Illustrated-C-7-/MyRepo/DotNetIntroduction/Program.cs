using System; // Подключаем фундаментальное пространство имён System, в котором находится класс Console.

// Программа компилируется в сборку (.exe), содержащую CIL-код и метаданные.
// CLR будет управлять её выполнением (управляемый код).
namespace DotNetIntroduction // Пространство имён упорядочивает код и предотвращает конфликты имён.
{
    // Класс Program — контейнер для метода Main, точки входа в приложение.
    class Program
    {
        // Main — точка входа, вызываемая CLR при запуске сборки.
        // CLR предоставляет управляемую среду: автоматическую сборку мусора, безопасность типов.
        static void Main(string[] args) // Параметр args содержит аргументы командной строки (может быть пустым).
        {
            // Демонстрация интерполяции строк (возможность, добавленная в C# 6.0).
            string demoVersion = "демо"; // Присваиваем строковую переменную.
            Console.WriteLine($"=== Часть 1. Введение в C# и .NET Framework ({demoVersion}) ==="); // Выводим заголовок, используя интерполяцию.
            Console.WriteLine(); // Пустая строка для улучшения читаемости.

            // ---------- 1.1 Основы .NET Framework ----------
            Console.WriteLine("1.1 Основы .NET Framework"); // Выводим название раздела.
            // Следующие строки поясняют ключевые компоненты платформы.
            Console.WriteLine("- CLR (Common Language Runtime) — среда выполнения, управляющая памятью, потоками, исключениями и безопасностью.");
            Console.WriteLine("- BCL (Base Class Library) — библиотека базовых классов, предоставляющая базовую функциональность (ввод-вывод, коллекции, работу со строками и т. д.).");
            Console.WriteLine(); // Отделяем раздел пустой строкой.

            // ---------- 1.2 Компиляция и выполнение ----------
            Console.WriteLine("1.2 Компиляция и выполнение"); // Название раздела.
            Console.WriteLine("- Исходный код компилируется в CIL (Common Intermediate Language) — промежуточный язык, не зависящий от процессора.");
            Console.WriteLine("- Assembly (сборка) — файл .exe или .dll, содержащий CIL-код и метаданные с описанием типов.");
            Console.WriteLine("- JIT (Just-In-Time) компилятор преобразует CIL в машинный код непосредственно перед выполнением метода.");
            Console.WriteLine("- Управляемый код (managed code) — код, выполняемый под контролем CLR (автоматическая сборка мусора, проверка типов).");
            Console.WriteLine("- Неуправляемый код (unmanaged code) — код, работающий вне CLR (например, вызов нативных Win32 DLL через P/Invoke).");
            Console.WriteLine();

            // ---------- 1.3 Common Language Infrastructure (CLI) ----------
            Console.WriteLine("1.3 Common Language Infrastructure (CLI)"); // Название раздела.
            Console.WriteLine("- CLI (Common Language Infrastructure) — открытый стандарт ECMA-335, описывающий инфраструктуру .NET.");
            Console.WriteLine("- CTS (Common Type System) — единая система типов, обеспечивающая совместимость объектов на разных языках .NET.");
            Console.WriteLine("- CLS (Common Language Specification) — подмножество CTS, правила для межъязыкового взаимодействия.");
            Console.WriteLine();

            // ---------- 1.4 Эволюция C# ----------
            Console.WriteLine("1.4 Эволюция C#"); // Название раздела.
            Console.WriteLine("- C# 1.0–5.0: базовые возможности, обобщения (generics), LINQ, async/await.");
            Console.WriteLine("- C# 6.0: интерполяция строк ($\"...\"), инициализаторы свойств, выражения-тела членов.");
            Console.WriteLine("- C# 7.0: кортежи, сопоставление с образцом (pattern matching), локальные функции, ref-возврат и ref-локальные переменные.");

            // ---------- Демонстрация возможностей C# 6.0 и 7.0 ----------
            // Локальная функция (C# 7.0) — метод, объявленный внутри другого метода.
            string FormatFeature(string feature, string version) // Локальная функция с двумя параметрами.
            {
                return $"{feature} (появилось в {version})"; // Интерполяция строки (C# 6.0) для форматирования сообщения.
            }

            Console.WriteLine(); // Отступ перед примерами.
            // Использование локальной функции для вывода информации об одной из возможностей.
            Console.WriteLine(FormatFeature("Интерполяция строк", "C# 6.0"));

            // Кортеж (C# 7.0) — позволяет вернуть из функции несколько значений без создания отдельного класса.
            (string LanguageName, int FirstReleaseYear) GetLanguageInfo() // Локальная функция, возвращающая кортеж.
            {
                return ("C#", 2002); // Создаём и возвращаем кортеж.
            }
            var langInfo = GetLanguageInfo(); // Неявная типизация (var) определяет тип кортежа.
            // Доступ к элементам кортежа по именам.
            Console.WriteLine($"Язык {langInfo.LanguageName} впервые появился в {langInfo.FirstReleaseYear} году.");

            // Сопоставление с образцом (pattern matching) — удобная проверка типа и извлечение значения (C# 7.0).
            object someObject = "Пример строки"; // Переменная базового типа object.
            // Проверяем, является ли объект строкой, и сразу присваиваем значение новой переменной.
            if (someObject is string text) // Сопоставление с образцом "is" с объявлением переменной.
            {
                Console.WriteLine($"Объект является строкой: \"{text}\""); // Выводим извлечённую строку.
            }

            // Завершение работы программы с ожиданием нажатия клавиши.
            Console.WriteLine("\nНажмите любую клавишу для выхода..."); // Приглашение.
            Console.ReadKey(); // Ожидание ввода, чтобы окно консоли не закрылось мгновенно.
        }
    }
}