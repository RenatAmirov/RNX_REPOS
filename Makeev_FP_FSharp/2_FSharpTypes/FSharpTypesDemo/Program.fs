// Program.fs
// Демонстрация основных составных типов данных: списки и кортежи

// Объявляем модуль верхнего уровня (необязательно, но хорошая практика)
module Demo

// Точка входа в программу — функция main с атрибутом EntryPoint
[<EntryPoint>]
let main argv =
    // argv — массив аргументов командной строки (в данном примере не используется)

    // ====================================================
    // 1. СПИСКИ
    // ====================================================
    // Список — упорядоченный набор элементов одного типа.
    // Строка в F# тоже является списком символов (seq<char>).

    // Создадим список целых чисел с помощью литерала: [1; 2; 3]
    let numbersList = [1; 2; 3]   // тип: int list
    // numbersList — переменная, связывается со значением списка [1;2;3]
    
    // Создадим строку — она эквивалентна списку символов
    let greetingString = "hello"   // тип: string, которая является seq<char>
    // greetingString содержит строку "hello", её можно рассматривать как список символов

    // Создание списка операцией cons (::) — добавление элемента в начало (голову)
    let oneAdded = 1 :: [2; 3]     // тип: int list
    // Оператор :: берёт левый операнд (1) и добавляет его в голову правого списка ([2;3])
    // Результат: 1 :: [2;3] -> [1;2;3]

    // Проверим эквивалентность: oneAdded должно равняться numbersList
    let areEqual = (numbersList = oneAdded)   // тип: bool, значение true
    // Оператор = сравнивает списки поэлементно

    // Выведем списки в консоль
    printfn "Списки:"
    printfn "  numbersList = %A" numbersList        // %A — универсальный формат для любого типа
    printfn "  greetingString = %A" greetingString  // выведет строку в кавычках: "hello"
    printfn "  oneAdded = %A" oneAdded              // [1; 2; 3]
    printfn "  numbersList = oneAdded ? %b" areEqual // %b — булево значение

    // Дополнительно: строку можно преобразовать в список символов явно
    let charList = Seq.toList greetingString        // тип: char list
    // Seq.toList берёт последовательность (строку) и создаёт список её элементов
    printfn "  charList = %A" charList              // ['h'; 'e'; 'l'; 'l'; 'o']

    // Бесконечный список в F# создать можно через ленивые последовательности (seq), 
    // но чисто список бесконечным не бывает в памяти.
    // Однако концептуально списки могут быть бесконечными в смысле генерации.
    // Покажем пример бесконечной последовательности (не список) для иллюстрации:
    let infiniteNumbers = Seq.initInfinite (fun i -> i) // seq<int>, 0,1,2,... бесконечно
    // Seq.initInfinite генерирует последовательность, вызывая функцию для i = 0,1,2,...
    // Взять первые 5 элементов:
    let firstFive = Seq.take 5 infiniteNumbers |> Seq.toList
    // Seq.take 5 — берёт 5 первых элементов последовательности, |> — передача результата в Seq.toList
    printfn "  Первые 5 элементов бесконечной последовательности: %A" firstFive

    // ====================================================
    // 2. КОРТЕЖИ
    // ====================================================
    // Кортеж — фиксированное количество элементов, возможно разных типов.
    // Пример: (1, 'a') :: (int, char)

    // Создадим кортеж из целого числа и символа
    let tuple1 = (1, 'a')               // тип: int * char
    // tuple1 — переменная, хранящая пару значений. В F# тип записывается как int * char.

    // Можно также явно указать тип (редко требуется)
    let tuple2 : (int * char) = (2, 'b')

    // Элементы кортежа можно извлечь через сопоставление с образцом или функции fst/snd (только для пар)
    let firstElement = fst tuple1       // fst возвращает первый элемент пары: 1
    let secondElement = snd tuple1      // snd возвращает второй элемент: 'a'

    // Выведем кортежи и их компоненты
    printfn "\nКортежи:"
    printfn "  tuple1 = %A" tuple1        // (1, 'a')
    printfn "  tuple2 = %A" tuple2        // (2, 'b')
    printfn "  первый элемент tuple1 = %d" firstElement   // %d — целое
    printfn "  второй элемент tuple1 = %c" secondElement  // %c — символ

    // Кортеж может содержать больше двух элементов
    let triple = ("F#", 2024, true)       // тип: string * int * bool
    printfn "  triple = %A" triple         // ("F#", 2024, true)

    // ====================================================
    // ЗАВЕРШЕНИЕ
    // ====================================================
    printfn "\nДемонстрация завершена. Нажмите любую клавишу..."
    System.Console.ReadKey() |> ignore   // ожидание нажатия клавиши, чтобы окно не закрылось
    0 // возврат кода 0 — успешное завершение программы