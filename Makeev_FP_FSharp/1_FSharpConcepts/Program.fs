// ================================================================
// Основные понятия функционального программирования в F#
// (аналогично языку Haskell, описанному в исходном тексте)
// ================================================================

// Модуль верхнего уровня (необязателен, но здесь для ясности)
module Main

// Точка входа в программу
[<EntryPoint>]
let main argv =
    // ------------------------------------------------------------
    // 1. Значения и типы
    // Значение — простейшие данные, которые нельзя упростить или
    // вычислить дальше. Каждое значение имеет тип.
    // В F# тип можно указать явно через аннотацию (имя : тип).
    // ------------------------------------------------------------

    // Целочисленное значение 5 типа int (аналог Integer)
    let intValue : int = 5
    // Читается: "5 имеет тип int", что соответствует записи 5 :: Integer

    // Логическое значение true типа bool (аналог Bool)
    let boolValue : bool = true
    // Читается: "true имеет тип bool"

    // Символьное значение 'a' типа char (аналог Char)
    let charValue : char = 'a'
    // Читается: "'a' имеет тип char"

    // Кортеж (1, true) — одно составное значение типа int * bool
    let tupleValue : int * bool = (1, true)
    // Тип произведения: int и bool

    // Список [1; 2; 3] — значение типа int list (аналог [1,2,3])
    let listValue : int list = [1; 2; 3]
    // В F# список — неизменяемый односвязный список

    // Вывод значений в консоль для демонстрации
    printfn "--- Значения и их типы ---"
    printfn "intValue   : %d (тип int)" intValue
    printfn "boolValue  : %b (тип bool)" boolValue
    printfn "charValue  : %c (тип char)" charValue
    printfn "tupleValue : %A (тип int * bool)" tupleValue
    printfn "listValue  : %A (тип int list)" listValue

    // ------------------------------------------------------------
    // 2. Переменные (неизменяемые имена)
    // В Haskell нет изменяемых переменных. Имена получают значение
    // один раз и не могут быть переопределены с новым значением.
    // F# по умолчанию тоже использует неизменяемые привязки let.
    // ------------------------------------------------------------

    // Попытка присвоить новое значение intValue приведёт к ошибке компиляции:
    // intValue <- 10
    // Ошибка: "This value is not mutable"

    // В F# можно создать изменяемую переменную явно, но это противоречит
    // чисто функциональному стилю, показанному в исходном тексте.
    // Поэтому мы придерживаемся только неизменяемых привязок.

    printfn "\n--- Неизменяемость переменных ---"
    printfn "intValue остаётся равным %d, его нельзя изменить" intValue

    // ------------------------------------------------------------
    // 3. Выражения и редукция (упрощение)
    // Выражение — конструкция, которую можно редуцировать до значения.
    // Пример из текста: (4+5)*2 → 9*2 → 18.
    // ------------------------------------------------------------

    // (4+5)*2 — это выражение, которое будет вычислено (редуцировано)
    // до значения 18 в момент связывания с именем computation.
    let computation = (4 + 5) * 2
    // Шаги редукции (в комментариях):
    // (4+5)*2 → 9*2 → 18

    printfn "\n--- Выражения и редукция ---"
    printfn "Выражение (4+5)*2 после редукции: %d" computation

    // Ещё один пример: условное выражение (if) – это тоже выражение,
    // которое редуцируется до одного из двух значений в зависимости от условия.
    let maxVal = if intValue > 3 then intValue else 0
    // Если intValue > 3, то выражение вернёт значение intValue (т.е. 5)
    // иначе вернёт 0. Фактически maxVal будет равно 5.

    printfn "Результат условного выражения if intValue > 3 then intValue else 0: %d" maxVal

    // Иллюстрация пошаговой редукции сложного выражения
    // Пусть есть функция квадрата числа
    let square x = x * x
    // Тогда выражение square(3+1) редуцируется так:
    // square(3+1) → square(4) → 4*4 → 16
    let result = square (3 + 1)
    printfn "Результат выражения square(3+1): %d (редукция: square(3+1) → square(4) → 4*4 → 16)" result

    // Завершение программы с кодом возврата 0 (успех)
    0